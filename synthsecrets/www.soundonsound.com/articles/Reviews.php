<li><a href="/search?url=%2Fsearch&Section=7&Subject=33&Summary=Yes"><b>Keyboard</b></a>&nbsp;&nbsp;includes all instruments with a keyboard (synths, Mellotrons, home keyboards, organs) <i>except</i> where they are more accurately described as a 'Keyboard Workstation'.</li>
<li><a href="/search?url=%2Fsearch&Section=7&Subject=34&Summary=Yes"><b>Keyboard Workstation</b></a>&nbsp;&nbsp;includes instruments like the Korg Trinity, Kurzweil K2000 etc 
that combine keyboard synths with onboard sequencers, sampling and such-like.</li>
<li><a href="/search?url=%2Fsearch&Section=7&Subject=53&Summary=Yes"><b>Sequencing Workstation</b></a>&nbsp;&nbsp;includes hardware devices like the MC505 that have sequencers plus sundry synths, controllers, effects but no keyboard.</li>
<li><a href="/search?url=%2Fsearch&Section=7&Subject=52&Summary=Yes"><b>Sampling Workstation</b></a>&nbsp;&nbsp;As above but where a device's emphasis is on sampling rather than sequencing.</li>
<li><a href="/search?url=%2Fsearch&Section=7&Subject=23&Summary=Yes"><b>Computer Recording System</b></a>&nbsp;&nbsp;eg. Soundscape, Yamaha DSP Factory, Lexicon Studio and others.</li>
<li><a href="/search?url=%2Fsearch&Section=7&Subject=22&Summary=Yes"><b>Computer</b></a></li>
<li><a href="/search?url=%2Fsearch&Section=7&Subject=51&Summary=Yes"><b>Sampler</b></a>&nbsp;&nbsp;hardware varieties only; see 'Software' for soft samplers.</li>
<li><a href="/search?url=%2Fsearch&Section=7&Subject=33&Summary=Yes"><b>Hardware Sequencer </b></a>&nbsp;&nbsp;includes analogue sequencers.</li>
<li><a href="/search?url=%2Fsearch&Section=7&Subject=41&Summary=Yes"><b>Modular Synth</b></a></li>
<li><a href="/search?url=%2Fsearch&Section=7&Subject=30&Summary=Yes"><b>Hardware Controller</b></li>
<li><a href="/search?url=%2Fsearch&Section=7&Subject=27&Summary=Yes"><b>Expansion Board</b></a>&nbsp;&nbsp;includes hardware boards for samplers, sound modules etc.</li>
<li><a href="/search?url=%2Fsearch&Section=7&Subject=55&Summary=Yes"><b>Sound Module</b></a>&nbsp;&nbsp;includes all non-keyboard hardware devices (except PC soundcards) the principal function of which is to produce sound, whether by synthesis or sample playback. Modular Synths have their own category.</li>
<li><a href="/search?url=%2Fsearch&Section=7&Subject=47&Summary=Yes"><b>Preamp</b></a>&nbsp;&nbsp;includes Mic Preamps and other kinds.</li>
<li><a href="/search?url=%2Fsearch&Words=All&Section=7&Subject=40&Summary=No"><b>Mixer</b></a>&nbsp;&nbsp;includes Digital, Analogue and Line-level hardware mixers.</li>
<li><a href="/search?url=%2Fsearch&Section=7&Subject=42&Summary=Yes"><b>Monitors</b></a></li>
<li><a href="/search?url=%2Fsearch&Section=7&Subject=24&Summary=Yes"><b>Drum Machine</b></a></li>
<li><a href="/search?url=%2Fsearch&Section=7&Subject=45&Summary=Yes"><b>PC Soundcard</b></a></li>
<li><a href="/search?url=%2Fsearch&Section=7&Subject=46&Summary=Yes"><b>PC/Mac Soundcard</b></a></li>
<li><a href="/search?url=%2Fsearch&Section=7&Subject=36&Summary=Yes"><b>MIDI Controller</b></a>&nbsp;&nbsp;includes MIDI guitars, wind controllers, fader-style control surfaces (like Steinberg's Houston) but <i>not</i> keyboards or complete electronic drum systems like the VDrum.</li><li><a href="/search?url=%2Fsearch&Section=7&Subject=26&Summary=Yes"><b>Electronic Drumkit</b></a></li>
