<li><a href="/search?url=%2Fsearch&Section=7&Subject=43&Summary=Yes"><b>Multitrack Recorder</b></a>&nbsp;&nbsp;includes all stand-alone <i>multitrack</i> systems ie. ADATs, Minidisc/Hard disk/Zip/Cassette multitrackers and reel-to-reel recorders. Systems that depend on computers, like Pro Tools or Soundscape, are found in the 'Computer Recording System' category.</li>
<li><a href="/search?url=%2Fsearch&Section=7&Subject=38&Summary=Yes"><b>MIDI Interface</b></li>
<li><a href="/search?url=%2Fsearch&Section=7&Subject=54&Summary=Yes"><b>Software</b></a>&nbsp;&nbsp;includes all computer software that is sold on its own, ie. sequencers, sound editing packages, plug-ins, audio-to-MIDI conversion, loop editing software, music scoring programs, soft synths/samplers, etc.</li>
<li><a href="/search?url=%2Fsearch&Section=7&Subject=48&Summary=Yes"><b>Processor</b></a>&nbsp;&nbsp;includes hardware-based Voice Channels, EQs, Compressors, Gates, Enhancers, other dynamics processors, De-essers, noise reduction suystems etc.</li>
<li><a href="/search?url=%2Fsearch&Section=7&Subject=25&Summary=Yes"><b>Effects</b></a>&nbsp;&nbsp;(hardware-only) includes multi-effects, reverb, delay, echo, flange, phase, ring modulation, harmonisers, pitch-shifters, vocoders etc.</li>
<li><a href="/search?url=%2Fsearch&Section=7&Subject=37&Summary=Yes"><b>MIDI File Player/Recorder</b></a>&nbsp;&nbsp;does <i>not</i> include hardware sequencers that permit MIDI data editing.</li>
<li><a href="/search?url=%2Fsearch&Section=7&Subject=56&Summary=Yes"><b>Sound/Song Library </b></a>&nbsp;&nbsp;includes any product (MIDI Files, cartridges, voice cards, soundbank disks) that provides new sounds/patches for an instrument.</li>
<li><a href="/search?url=%2Fsearch&Section=7&Subject=50&Summary=Yes"><b>Sample Shop</b></a>&nbsp;&nbsp;this is the umbrella title for the magazine section where we generally review all Sample CDs, Refills etc.</li>
