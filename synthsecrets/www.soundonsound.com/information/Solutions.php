<div class="xhead">Logging in</div>
<p><li><b>I am clicking the 'Remember me' box when I log in, but when I come back to the site the login box doesn't display my login ID (ie. email or Subscriber PIN)</b><br />This facility requires a permanent cookie to be placed on your computer by our site. If you have turned cookies off, or have a firewall which intercepts/blocks cookies, this feature cannot work. For more detail, see our <a href="Cookies.php">Cookies help page</a> on this subject.</p>

<div class="xhead">Viewing eSub-only articles</div>
<p><li><b>I have logged in to view eSub-only articles, but I keep getting taken to the login screen</b><br />This is most likely a problem with cookies on your browser, due to either your security settings or a firewall. For more detail, see our <a href="Cookies.php">Cookies help page</a> on this subject.</p>

<div class="xhead">Readers' Adverts</div>
<p><li><b>I am trying to submit a new Readers' Advert, but I keep getting the login screen</b><br />You need to register first (it's free) or login to the site if you are already a subscriber. This is most likely a cookie problem. See our <a href="Cookies.php">Cookies help page</a> on this topic.</p>

<p><li><b>I can only see the WANTED adverts and there are no category links/buttons down the left side, only totals</b><br />This is nearly always caused by a firewall (XP has one built-in) or ad-blocking software such as AdAware, Norton Security/Personal Firewall, Zone Alarm Pro etc blocking the URLs used in these links. The solution is to ensure that the following URLs/domains are entered as Allowed/Trusted Sites in your firewall and/or security software:<br/>
<br/>http://www.soundonsound.com<br/>
http://www.soundonsound.com/adverts</p>
<p>
<li><b>I have sent an email to an advertiser, but I haven't heard anything back &mdash; is there a problem?</b><br />
There are a number of possible reasons why you could not have had a reply from an advertiser:<dir>
&#149; You have mistyped your own email address for them to reply to (unlikely, of course!)<br />
&#149; The advertiser's email address is invalid &mdash; we verify email addresses as far as possible, but it is difficult to spot every rogue one!<br />
&#149; The advertiser simply is not replying for reasons of their own<br />
</dir>
</p>
<p>If email is the only means of contact provided by an advertiser and it doesn't appear to work, we are sorry but we are unable to assist in putting you in contact with them due to Data Protection laws. However, we are happy to send them your email address if you request it of us (contact <a href="mailto:webmaster@soundonsound.com">webmaster@soundonsound.com</a>).</p>
<div class="xhead">Shopping</div>
<p><li><b>My shopping basket is empty even though I just added a product to it</b><br />This is most likely a problem with cookies on your browser, due to either your security settings or a firewall. Try Refreshing the web page while holding down the ALT key. For more detail, see our <a href="../Cookies.php">Cookies help page</a> on this subject.</p> 
