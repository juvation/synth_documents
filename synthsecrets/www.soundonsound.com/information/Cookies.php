<p><b>What is a Cookie?</b><br/>
Most web sites store information in a small text file, called a <b>Cookie</b>, on your hard disk. Cookies typically contain information about you and your preferences for the web site you are visiting, so that the site can customise the web pages you see the next time you visit or go back to a previously visited part of the site.</p>
<p>A cookie can only store information you provide during your visit, based on the choices you make. A site cannot capture your email address unless you send it to the site, in a web form, for example. Allowing a web site to create a cookie does not give the site access to the rest of your computer, and only the site that created the cookie can read it. Credit card information is never stored in cookies.</p>
<p><b>How do SOS use Cookies?</b><br/>
The SOS site keeps the use of cookies to an absolute minimum, using them in only 3 places:</p>
<p><b>1. "Remember me" on the login form</b>  If you choose to check this box, a permanent cookie called "SOS[Login]" is stored on your computer and you will find that the box labelled "Sub PIN or Email" will contain your login the next time you return to the site. This is simply to enable you to log in quicker and with less typing. Note that if you click ONLY this option, your password is NOT stored - only the login itself.</p>
<p>It is recommended that you do not check this box if your computer is shared with other people who you do not know (e.g. in an internet café or on a university campus).</p>
<p><b>2. "Stay logged in" on the login form</b>  If you choose to check this box, a permanent cookie called "SOS[Persist]" is stored on your computer, containing your login (i.e. email address or Sub PIN) and encrypted authentication information. You will now remain permanently logged in to the site, and you will be automatically logged back into the site each time you return to it. You can logout at any time by clicking the 'Logout' button on the right-hand side of the screen, or under 'My SOS' in the black menus.</p>
<p>It is <b>essential</b> that you do not check this box if your computer is shared with other people.</p>
<p><b>3. Category selection in Readers' Ads</b> To help you submit multiple adverts a bit quicker, we keep track of the category of your last submitted advert and pre-select it next time you post an advert (assuming you don't close your browser in between, or otherwise start a new session).</p>
<p>You can safely turn off cookies for our site should you wish, and the functionality will not be compromised except with respect to these two features.</p>
<p><b>Login benefits</b><br/>
Whenever you fill in web forms, many sites store such information in a cookie on your computer and use it to automatically populate similar forms the next time you visit. The downside of this approach is that anyone else with access to your computer's browser might be able to masquerade as you when visiting that same site. To prevent this and maximise security, the SOS site utilises a Registration/Login system instead.<p>You only need to register <i>once</i> on the main SOS site. You can volunteer to register at any time &mdash; just click the <a href="/UserAccount/NewUser.php">Register Now</a> link in the Login box on the rightside of any SOS web page. But if you ever elect to subscribe, or enter an SOS Competition, or submit your own Readers Ads, or purchase goods from the SOS Shop, it will be necessary to supply us with your personal contact details so that we know where to send your prize, shop order or magazine subscription, and when to unlock the subscribers-only premium content on the site. This information is transmitted in encrypted format and stored on our secure servers, not in a cookie, in a personalised area called 'My Account'. We do not store credit card data.</p>
<p>You login using the Email address you entered on the My Account registration form, or your Subscriber PIN number (if you are a subscriber), plus your chosen Password. Your password is hidden from onlookers while you type it and you can change it as many times as you like. If you forget it, there's a password reminder link in the Login box on every page. SOS administrators have the power to overwrite your password if you really get stuck! Clicking the logout link or closing your browser will log you out of the site.</p>
<p class="mediumgreytext">Our aim at SOS is to make your visits to the site as frequent and as pleasurable as possible, and confidentiality and your privacy are both assured and respected at all times.</p>
