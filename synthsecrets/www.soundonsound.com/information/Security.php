<p>We take privacy and confidentiality seriously, that's why the SOUND ON SOUND web site use 128-bit SSL encryption via Secure HTTP (https:) pages anywhere that private information or credit card data is required to be submitted by any user of the site.</p>
<p>When you register with the SOS site (or become a subscriber or shopper) you create a unique <b>My Account</b> record for yourself. All transmission of sensitive data to and from the SOS site (such as when you update your My Account information, buy something in our Shop, change passwords, address details and such-like) takes place within a secure, SSL-encrypted, environment. Your browser should display a locked/closed padlock icon in its status bar whenever Secure HTTP connections are being used. Where forms are used for you to submit data to the site, the <img src="/images/shop/secure.gif" height="40" width="40" align="left" vspace="3" hspace="3" border="0" alt="SOS Secure logo" />
helps you identify such pages. PC users may verify the security of any web page by right-clicking on such pages and selecting &quot;Properties&quot; from the pop-up menu. The resulting window explains what level of encryption is being used on that page. Typically, on SOS secure pages, this will show something like:</p>
<blockquote class="smallgreytext">SSL 3.0, RC4 with 128 bit encryption (High); RSA with 1024 bit exchange</blockquote>
