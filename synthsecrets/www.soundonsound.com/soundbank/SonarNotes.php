<p class="bodytext">This example accompanies the section called &#0147;The New Ambience&#0148; in the February 2004 Sonar Notes. You&#0146;ll first hear two measures of a drum loop with no delays, a brief pause, then two measures processed by the FxDelay settings shown in the table. The difference is subtle &ndash; listen carefully &ndash; but note how the processed version has a better stereo image and improved ambience. The second part of the track plays an acoustic guitar for two measures with no delays, then inserts a brief pause, then adds some FxChorus delay using the settings shown in the second table.</p>
